class Employees {

    public static int[] employeeIds(int[] ids) {
        System.out.println("Employee IDs");
        return ids;
    }

    public static long[] employeeSalary(long[] salary) {
        System.out.println("Employee Salary");
        return salary;
    }

    public static double[] performanceRatings(double[] ratings) {
        System.out.println("Employee Performance Ratings");
        return ratings;
    }

    public static float[] workingHours(float[] hours) {
        System.out.println("Employee Working Hours");
        return hours;
    }

    public static char[] departmentCodes(char[] departments) {
        System.out.println("Employee Department Codes");
        return departments;
    }

    public static boolean[] isOnLeave(boolean[] leaveStatus) {
        System.out.println("Employees on Leave Today");
        return leaveStatus;
    }

    public static String[] employeeNames(String[] names) {
        System.out.println("Employee Names");
        return names;
    }
}
