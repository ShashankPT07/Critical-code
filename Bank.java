class Bank{

    public static int[] accountNumber(int[] number){
        System.out.println("Account Numbers");
        return number;
    }

    public static long[] accountBalance(long[] balance){
        System.out.println("Account Balances");
        return balance;
    }

    public static double[] interestRate(double[] rate){
        System.out.println("Interest Rates");
        return rate;
    }

    public static float[] loanAmount(float[] loan){
        System.out.println("Loan Amounts");
        return loan;
    }

    public static char[] accountType(char[] type){
        System.out.println("Account Types");
        return type;
    }

    public static boolean[] isActiveAccount(boolean[] active){
        System.out.println("Active Account Status");
        return active;
    }

    public static String[] bankName(String[] name){
        System.out.println("Bank Names");
        return name;
    }
}
