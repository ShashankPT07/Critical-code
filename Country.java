class Country{

    public static int[] countryCode(int[] code){
        System.out.println("Country Codes");
        return code;
    }

    public static long[] population(long[] population){
        System.out.println("Population");
        return population;
    }

    public static double[] area(double[] area){
        System.out.println("Area in square kilometers");
        return area;
    }

    public static float[] gdp(float[] gdp){
        System.out.println("GDP in billions");
        return gdp;
    }

    public static char[] continentCode(char[] code){
        System.out.println("Continent Codes");
        return code;
    }

    public static boolean[] isDeveloped(boolean[] developed){
        System.out.println("Is Developed Country");
        return developed;
    }

    public static String[] countryName(String[] name){
        System.out.println("Country Names");
        return name;
    }
}
