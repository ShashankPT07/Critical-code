class Animal {

    public static void name() {
        System.out.println("animal has a name");
    }

    public static void eat() {
        System.out.println("animal eats food");
    }

    public static void sleep() {
        System.out.println("animal sleeps at night");
    }

    public static void walk() {
        System.out.println("animal walks on legs");
    }

    public static void run() {
        System.out.println("animal can run fast");
    }

    public static void sound() {
        System.out.println("animal makes sounds");
    }

    public static void breathe() {
        System.out.println("animal breathes air");
    }

    public static void reproduce() {
        System.out.println("animal reproduces");
    }

    public static void grow() {
        System.out.println("animal grows over time");
    }

    public static void die() {
        System.out.println("animal eventually dies");
    }
}