class Computer {

    public static void boot() {
        System.out.println("computer boots up");
    }

    public static void shutdown() {
        System.out.println("computer shuts down");
    }

    public static void restart() {
        System.out.println("computer restarts");
    }

    public static void sleep() {
        System.out.println("computer enters sleep mode");
    }

    public static void openApp() {
        System.out.println("application opened");
    }

    public static void closeApp() {
        System.out.println("application closed");
    }

    public static void saveFile() {
        System.out.println("file saved");
    }

    public static void deleteFile() {
        System.out.println("file deleted");
    }

    public static void connectInternet() {
        System.out.println("connected to internet");
    }

    public static void disconnectInternet() {
        System.out.println("disconnected from internet");
    }
}

