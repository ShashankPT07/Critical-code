class Library {

    public static void open() {
        System.out.println("library is open");
    }

    public static void close() {
        System.out.println("library is closed");
    }

    public static void lendBook() {
        System.out.println("book lent to member");
    }

    public static void returnBook() {
        System.out.println("book returned by member");
    }

    public static void addBook() {
        System.out.println("new book added to library");
    }

    public static void removeBook() {
        System.out.println("book removed from library");
    }

    public static void organizeShelves() {
        System.out.println("library shelves organized");
    }

    public static void conductReadingSession() {
        System.out.println("reading session conducted");
    }

    public static void chargeFine() {
        System.out.println("fine charged for late return");
    }

    public static void registerMember() {
        System.out.println("new member registered");
    }
}




