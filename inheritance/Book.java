class Book {

    public static void open() {
        System.out.println("book opened");
    }

    public static void close() {
        System.out.println("book closed");
    }

    public static void read() {
        System.out.println("reading book");
    }

    public static void writeNotes() {
        System.out.println("writing notes");
    }

    public static void bookmark() {
        System.out.println("bookmark placed");
    }

    public static void tearPage() {
        System.out.println("page torn");
    }

    public static void flipPage() {
        System.out.println("page flipped");
    }

    public static void lend() {
        System.out.println("book lent");
    }

    public static void returnBook() {
        System.out.println("book returned");
    }

    public static void highlight() {
        System.out.println("text highlighted");
    }
}


