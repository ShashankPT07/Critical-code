class Hospital {

    public static int[] hospitalId(int[] id) {
        System.out.println("Hospital ID");
        return id;
    }

    public static long[] patientCount(long[] patients) {
        System.out.println("Total number of patients");
        return patients;
    }

    public static double[] medicineCost(double[] cost) {
        System.out.println("Cost of medicines");
        return cost;
    }

    public static float[] hospitalRating(float[] rating) {
        System.out.println("Hospital ratings");
        return rating;
    }

    public static char[] departmentCode(char[] departments) {
        System.out.println("Hospital department codes");
        return departments;
    }

    public static boolean[] isEmergencyAvailable(boolean[] emergencyStatus) {
        System.out.println("Emergency services available");
        return emergencyStatus;
    }

    public static String[] hospitalName(String[] name) {
        System.out.println("Hospital names");
        return name;
    }
}
