class Movie {

    public static int[] movieId(int[] id) {
        System.out.println("Movie ID");
        return id;
    }

    public static long[] boxOfficeCollection(long[] collections) {
        System.out.println("Box Office Collections ");
        return collections;
    }

    public static double[] movieRating(double[] ratings) {
        System.out.println("Movie Ratings");
        return ratings;
    }

    public static float[] duration(float[] durationInHours) {
        System.out.println("Movie Durations ");
        return durationInHours;
    }

    public static char[] movieGrade(char[] grades) {
        System.out.println("Movie Grades");
        return grades;
    }

    public static boolean[] isReleased(boolean[] releaseStatus) {
        System.out.println("Movie Release Status");
        return releaseStatus;
    }

    public static String[] movieTitle(String[] titles) {
        System.out.println("Movie Titles");
        return titles;
    }
}
