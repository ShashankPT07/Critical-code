class Flight{

    public static int[] flightNumber(int[] number){
        System.out.println("Flight Numbers");
        return number;
    }

    public static long[] flightDuration(long[] duration){
        System.out.println("Flight Duration in minutes");
        return duration;
    }

    public static double[] ticketPrice(double[] price){
        System.out.println("Ticket Prices");
        return price;
    }

    public static float[] fuelCapacity(float[] capacity){
        System.out.println("Fuel Capacity in liters");
        return capacity;
    }

    public static char[] airlineCode(char[] code){
        System.out.println("Airline Codes");
        return code;
    }

    public static boolean[] isOnTime(boolean[] onTime){
        System.out.println("On-Time Status");
        return onTime;
    }

    public static String[] destinationName(String[] name){
        System.out.println("Destination Names");
        return name;
    }
}
